    private void hienthiTG() {
        ArrayList<TacGia_pojo> dsTG = TacGia_dao.layDanhSachTacGia();
        dtm.setRowCount(0);
        for (TacGia_pojo pb : dsTG) {
            Vector<Object> v = new Vector<Object>();
            v.add(pb.getMaTG());
            v.add(pb.getTenTG());
            dtm.addRow(v);
        }
        tbTG.setModel(dtm);
    }

    public static boolean isChar(String s) {
        for (int i = 0; i < s.length(); i++) {
            if (!Character.isDigit(s.charAt(i))) {
                return false;
            } else {
                return true;
            }
        }
        return true;
    }
