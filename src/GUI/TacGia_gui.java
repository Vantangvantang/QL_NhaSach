    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        try {
            int row = tbTG.getSelectedRow();
            if (row == -1) {
                JOptionPane.showMessageDialog(this, "Chọn Dòng Cần Xóa ");
                return;
            } else {
                int n = JOptionPane.showConfirmDialog(this,"Chắc Xóa Chưa","Thông Báo", JOptionPane.YES_NO_OPTION);
                if (n == JOptionPane.NO_OPTION) {
                    return;
                } else {
                    String ma = (String) tbTG.getValueAt(row, 0);
                    if (TacGia_dao.xoaTacGia(ma) == true) {
                        TacGia_dao.xoaTacGia(ma);
                        hienthiTG();
                        JOptionPane.showMessageDialog(this, "Xoá Thành Công", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(this, "Bạn không có quyền xóa tác giả", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
            }
            hienthiTG();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Liên Hệ Nhóm 1 Để Biết Thông Tin Chi Tiết", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        try {
            String maTG = txtMaTG.getText();
            String tenTG = txtTenTG.getText();
            TacGia_pojo tg = new TacGia_pojo();
            tg.setMaTG(maTG);
            tg.setTenTG(tenTG);
            if (maTG.isEmpty() || tenTG.isEmpty()) {
                txtMaTG.requestFocus();
                JOptionPane.showMessageDialog(this, "Không Được Để Trống Thông Tin", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
            } else if (isChar(tenTG)) {
                JOptionPane.showMessageDialog(this, " Tên không được nhập số ", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
            } else if (TacGia_dao.ktraMaTG(maTG) == true) {
                try {
                     if(TacGia_dao.capNhatTacGia(tg)==true){
                       TacGia_dao.capNhatTacGia(tg);
                       hienthiTG();
                       JOptionPane.showMessageDialog(this, "Cập Nhật Thành Công", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);  
                    }else{
                       JOptionPane.showMessageDialog(this, "Bạn không có quyền cập nhật tác giả", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Cập Nhật Không Thành Công", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                  if (TacGia_dao.themTacGia(tg) == true) {
                    TacGia_dao.themTacGia(tg);
                    hienthiTG();
                    JOptionPane.showMessageDialog(this, "Thêm Tác Giả Thành Công", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Bạn không có quyền thêm tác giả", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Liên Hệ Nhóm 1 Để Biết Thông Tin Chi Tiết", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnLuuActionPerformed

