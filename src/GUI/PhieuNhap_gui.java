    private void hienthiPN() {
        ArrayList<PhieuNhap_pojo> dsPN = PhieuNhap_dao.layDanhSachPhieuNhap();
        dtmPN.setRowCount(0);
        for (PhieuNhap_pojo pb : dsPN) {
            Vector<Object> v = new Vector<Object>();
            v.add(pb.getMaPhieuNhap());
            v.add(pb.getNgayNhap());
            v.add(pb.getTongTien());
            v.add(pb.getMaNV());
            dtmPN.addRow(v);
        }
        tbPN.setModel(dtmPN);
    }

    private void hienthiCT() {
        ArrayList<ChiTietPhieuNhap_pojo> dsPN = ChiTietPhieuNhap_dao.layDanhSachCTPhieuNhap(txtMaPN.getText());
        dtmCT.setRowCount(0);
        for (ChiTietPhieuNhap_pojo pb : dsPN) {
            Vector<Object> v = new Vector<Object>();
            v.add(pb.getMaPhieuNhap());
            v.add(pb.getMaSach());
            v.add(pb.getSoLuong());
            dtmCT.addRow(v);
        }
        tbCT.setModel(dtmCT);
    }

