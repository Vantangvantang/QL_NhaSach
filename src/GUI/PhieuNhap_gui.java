    private void btnLuuHDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuHDActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat formater = new SimpleDateFormat("yyyy/MM/dd");
        String ngayNhap = formater.format(txtNgayNhap.getValue());
        if (txtMaPN.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không Được Để Trống Thông Tin", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            if (PhieuNhap_dao.ktraMaPN(txtMaPN.getText())) {
                boolean suaHD = PhieuNhap_dao.suaPN(txtMaPN.getText(), ngayNhap);
                if (suaHD) {
                    JOptionPane.showMessageDialog(this, "Sửa phiếu nhập thành công");
                    hienthiPN();
                } else {
                    JOptionPane.showMessageDialog(this, "Sửa phiếu nhập thất bại");
                }
            } else {
                boolean themHD = PhieuNhap_dao.themPN(txtMaPN.getText(), ngayNhap);
                if (themHD) {
                    JOptionPane.showMessageDialog(this, "Thêm phiếu nhập thành công");
                    hienthiPN();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm phiếu nhập thất bại");
                }
            }
            btnLuuHD.setEnabled(false);
        }
    }//GEN-LAST:event_btnLuuHDActionPerformed

    private void btnXoaHDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaHDActionPerformed
        // TODO add your handling code here:
        try {
            int row = tbPN.getSelectedRow();
            if (row == -1) {
                JOptionPane.showMessageDialog(this, "Chọn Dòng Cần Xóa ");
                return;
            } else {
                int n = JOptionPane.showConfirmDialog(this,"Chắc Xóa Chưa", "Thông Báo", JOptionPane.YES_NO_OPTION);
                if (n == JOptionPane.NO_OPTION) {
                    return;
                } else {
                    PhieuNhap_dao.xoaPN(txtMaPN.getText());
                    hienthiPN();
                    JOptionPane.showMessageDialog(this, "Xoá Thành Công", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                }
            }
            hienthiPN();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Liên Hệ Nhóm 1 Để Biết Thông Tin Chi Tiết", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnXoaHDActionPerformed

