    private void hienthiHD() {
        ArrayList<HoaDon_pojo> dsHD = HoaDon_dao.layDanhSachHoaDon();
        dtmHD.setRowCount(0);
        for (HoaDon_pojo pb : dsHD) {
            Vector<Object> v = new Vector<Object>();
            v.add(pb.getMaHD());
            v.add(pb.getNgayLap());
            v.add(pb.getTongTien());
            v.add(pb.getMaKH());
            v.add(pb.getMaNV());
            v.add(pb.getTinhTrang());
            dtmHD.addRow(v);
        }
        tbHD.setModel(dtmHD);
    }

    private void hienthiCT() {
        ArrayList<ChiTietHoaDon_pojo> dsHD = ChiTietHoaDon_dao.layDanhSachChiTiet(txtMaHD.getText());
        dtmCT.setRowCount(0);
        for (ChiTietHoaDon_pojo pb : dsHD) {
            Vector<Object> v = new Vector<Object>();
            v.add(pb.getMaHD());
            v.add(pb.getMaS());
            v.add(pb.getSoLuong());
            dtmCT.addRow(v);
        }
        tbCT.setModel(dtmCT);
    }

