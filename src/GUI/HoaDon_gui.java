    private void btnXuatHoaDonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXuatHoaDonActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            int TongSL = 0;
            String maHD = txtMaHD.getText();
            if (maHD == "") {
                JOptionPane.showMessageDialog(this, "Nhập mã hoá đơn muốn xuất!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            FileWriter fw = new FileWriter("HoaDon_" + maHD + ".txt");
            BufferedWriter bw = new BufferedWriter(fw);

            ArrayList<XuatHoaDon_pojo> dshd = HoaDon_dao.xuatHoaDon(maHD);
            for (XuatHoaDon_pojo hd : dshd) {
                bw.write("               Cửa hàng nhà sách HUFI");
                bw.newLine();
                bw.write("Đ/c: 140, Lê Trọng Tấn, Tây Thạnh, Tân Phú, TPHCM");
                bw.newLine();
                bw.write("Mã hoá đơn: " + hd.getMaHD());
                bw.newLine();
                SimpleDateFormat formater = new SimpleDateFormat("dd/MM/yyyy hh:mm:ss");
                String ngaylap = formater.format(hd.getNgayLap());
                bw.write("Ngày lập: " + ngaylap);
                bw.newLine();
                bw.write("Mô tả: ");
                bw.newLine();
                bw.write("=================================================");
                bw.newLine();
                break;
            }
            for (XuatHoaDon_pojo hd : dshd) {
                TongSL = TongSL + hd.getSoLuongMua();
                bw.write("" + hd.getTenSach() + " | " + hd.getSoLuongMua() + " | " + hd.getDonGia());
                bw.newLine();
            }
            for (XuatHoaDon_pojo hd : dshd) {
                bw.write("-------------------------------------------------");
                bw.newLine();
                bw.write("Tổng số lượng: " + TongSL);
                bw.newLine();
                bw.write("Tổng tiền: " + hd.getTongTien());
                bw.newLine();
                bw.write("=================================================");
                bw.newLine();
                bw.write("Xin cám ơn quý khách!");

                bw.close();
                fw.close();
                break;
            }
            JOptionPane.showMessageDialog(this, "Xuất hoá đơn thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Xuất hoá đơn thất bại!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnXuatHoaDonActionPerformed

