    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        try {
            int row = tbNV.getSelectedRow();
            if (row == -1) {
                JOptionPane.showMessageDialog(this, "Chọn Dòng Cần Xóa ");
                return;
            } else {
                int n = JOptionPane.showConfirmDialog(this,"Chắc Xóa Chưa" ,"Thông Báo", JOptionPane.YES_NO_OPTION);
                if (n == JOptionPane.NO_OPTION) {
                    return;
                } else {
                    String ma = (String) tbNV.getValueAt(row, 0);
                    NhanVien_dao.xoaNhanVien(ma);
                    hienthiNV();
                    JOptionPane.showMessageDialog(this, "Xoá Thành Công", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                }
            }
            hienthiNV();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Liên Hệ Nhóm 1 Để Biết Thông Tin Chi Tiết", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        try {
            String maNV = txtMaNV.getText();
            String tenNV = txtTenNV.getText();
            String ccCD = txtCCCD.getText();
            String sDT = txtSDT.getText();
            String thamNien = txtThamNien.getValue().toString();
            String diaChi = txtDiaChi.getText();
            String eMail = txtEmail.getText();
            String chucVu = null;
            if (rdoKho.isSelected()) {
                chucVu = rdoKho.getText();
            } else if (rdoThuNgan.isSelected()) {
                chucVu = rdoThuNgan.getText();
            } else {
                JOptionPane.showMessageDialog(this, "Chọn chức vụ", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            String matKhau = txtMatKhau.getText();
            NhanVien_pojo nv = new NhanVien_pojo();
            nv.setMaNV(maNV);
            nv.setTenNV(tenNV);
            nv.setCccd(ccCD);
            nv.setSdtNV(sDT);
            nv.setThamNien(thamNien);
            nv.setDiaChi(diaChi);
            nv.setEmail(eMail);
            nv.setChucVu(chucVu);
            nv.setMatKhau(matKhau);
            if (txtMaNV.getText().isEmpty() || txtTenNV.getText().isEmpty() || txtCCCD.getText().isEmpty() || txtSDT.getText().isEmpty() || txtDiaChi.getText().isEmpty() || txtEmail.getText().isEmpty() || (!rdoKho.isSelected() && !rdoThuNgan.isSelected()) || txtMatKhau.getText().isEmpty()) {
                txtMaNV.requestFocus();
                JOptionPane.showMessageDialog(this, "Không Được Để Trống Thông Tin", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
            } else {
                if (isChar(txtTenNV.getText())) {
                    JOptionPane.showMessageDialog(this, "Tên Không được nhập số ", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                } else if (isNumeric(txtCCCD.getText())) {
                    JOptionPane.showMessageDialog(this, "CCCD Không được nhập chữ ", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                } else if (isNumeric(txtSDT.getText())) {
                    JOptionPane.showMessageDialog(this, "SDT Không được nhập chữ ", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                } else if (isNumeric(txtThamNien.getValue().toString())) {
                    JOptionPane.showMessageDialog(this, " Thâm Niên Không được nhập chữ ", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                } else if (NhanVien_dao.ktraMaNV(maNV) == true) {
                    try {
                        NhanVien_dao.capNhatNhanVien(nv);
                        JOptionPane.showMessageDialog(this, "Cập Nhật Thành Công", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                        hienthiNV();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(this, "Cập Nhật Không Thành Công", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                    }
                } else {
                    try {
                        NhanVien_dao.themNhanVien(nv);
                        hienthiNV();
                        JOptionPane.showMessageDialog(this, "Thêm Nhân Viên Thành Công", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                        btnThem.setEnabled(true);
                        btnLuu.setEnabled(false);
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(this, "Thêm Nhân Viên Không Thành Công", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Liên Hệ Nhóm 1 Để Biết Thông Tin Chi Tiết", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnLuuActionPerformed

