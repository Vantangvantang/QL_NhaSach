    private void hienthiNV() {
        ArrayList<NhanVien_pojo> dsNV = NhanVien_dao.layDanhSachNhanVien();
        dtmNV.setRowCount(0);
        for (NhanVien_pojo pb : dsNV) {
            Vector<Object> v = new Vector<Object>();
            v.add(pb.getMaNV());
            v.add(pb.getTenNV());
            v.add(pb.getCccd());
            v.add(pb.getSdtNV());
            v.add(pb.getThamNien());
            v.add(pb.getDiaChi());
            v.add(pb.getEmail());
            v.add(pb.getChucVu());
            v.add(pb.getMatKhau());
            dtmNV.addRow(v);
        }
        tbNV.setModel(dtmNV);
    }
    
    public static boolean isNumeric(String s) {
        for (int i = 0; i < s.length(); i++) {
            if (!Character.isDigit(s.charAt(i))) {
                return true;
            } else {
                return false;
            }
        }
        return false;
    }
    
    public static boolean isChar(String s) {
        for (int i = 0; i < s.length(); i++) {
            if (!Character.isDigit(s.charAt(i))) {
                return false;
            } else {
                return true;
            }
        }
        return true;
    }
